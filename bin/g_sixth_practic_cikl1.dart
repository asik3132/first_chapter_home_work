void main() {
  //Необходимо вывести на экран числа от 1 до 5
  for (int i = 1; i <= 5; i++) {
    print(i);
  }

//Необходимо вывести на экран числа от 5 до 1
  for (int i = 5; i >= 1; i--) {
    print(i);
  }

//Необходимо вывести на экран таблицу умножения на 3
  for (int i = 1; i <= 10; i++) {
    print('3*$i=${3 * 10}');
  }
}
